package chapter2;

public class Constant {
    public static void main(String[] args) {
        final int MAX_NUM = 100; // Declare and initialize MAX_NUM --> This is Constant(상수= 변하지 않는값) final 예약어 
        final int MIN_NUM;
        MIN_NUM = 0;


        System.out.println(MAX_NUM);
        System.out.println(MIN_NUM);


    }
}
