package Chapter4;

public class SwitchCase2 {
    public static void main(String[] args) {
        String medal ="Gold";

        switch(medal){
            case "Gold" : System.out.println("Gold medal");
                break;
            case "Silver" :System.out.println("Silver medal"); 
                break;
            case "Bronze" :System.out.println("Bronze medal");
                break;
            default :
                break;
        }
    }
}
